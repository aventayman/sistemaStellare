package it.unibs.fp.sistemaStellare;

public class Posizione {
    private int x, y;

    public Posizione () {}

    public Posizione (int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
